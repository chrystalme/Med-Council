class PlanGuardrailTest(unittest.TestCase):
    def _check(self, text: str) -> GuardrailFunctionOutput:
        return _run(g._check_plan_structure(_StubCtx(), _StubAgent(), text))

    def test_all_six_headers_passes(self) -> None:
        result = self._check(_valid_plan_text())
        self.assertFalse(result.tripwire_triggered)
        self.assertEqual(len(result.output_info["found"]), 6)

    def test_five_of_six_passes(self) -> None:
        text = _valid_plan_text().replace("## MEDICATIONS TO CONSIDER", "")
        result = self._check(text)
        self.assertFalse(result.tripwire_triggered)

    def test_four_of_six_trips(self) -> None:
        text = (
            _valid_plan_text()
            .replace("## MEDICATIONS TO CONSIDER", "")
            .replace("## LIFESTYLE MODIFICATIONS", "")
        )
        result = self._check(text)
        self.assertTrue(result.tripwire_triggered)
        self.assertEqual(result.output_info["code"], "plan_structure_invalid")
        self.assertGreaterEqual(len(result.output_info["missing_headers"]), 2)

    def test_case_insensitive_match(self) -> None:
        text = _valid_plan_text().replace("## IMMEDIATE ACTIONS", "## immediate actions")
        result = self._check(text)
        self.assertFalse(result.tripwire_triggered)

    def test_trailing_colon_match(self) -> None:
        text = _valid_plan_text().replace(
            "## IMMEDIATE ACTIONS", "## IMMEDIATE ACTIONS:"
        )
        result = self._check(text)
        self.assertFalse(result.tripwire_triggered)


