class ToxicityGuardrailTest(unittest.TestCase):
    def _check(self, output: str, classifier_response: str) -> GuardrailFunctionOutput:
        async def _fake_run_agent_raw(*args, **kwargs):
            return classifier_response

        # Make sure the lazy import inside the guardrail picks up our stub.
        import main as _main

        with patch.object(_main, "run_agent_raw", side_effect=_fake_run_agent_raw):
            return _run(g._check_toxicity(_StubCtx(), _StubAgent(), output))

    def test_safe_classifier_passes(self) -> None:
        result = self._check(
            "Patient has migraine; recommend hydration.",
            '{"is_safe": true, "category": "safe", "reasoning": "ok"}',
        )
        self.assertFalse(result.tripwire_triggered)

    def test_refusal_classifier_trips(self) -> None:
        result = self._check(
            "I cannot help with medical advice.",
            '{"is_safe": false, "category": "refusal", "reasoning": "refusal contradicts scope"}',
        )
        self.assertTrue(result.tripwire_triggered)
        self.assertEqual(result.output_info["code"], "toxicity_flagged")
        self.assertEqual(result.output_info["category"], "refusal")

    def test_classifier_error_passes_through(self) -> None:
        # If the classifier itself raises, don't block real output — pass with annotation.
        async def _boom(*args, **kwargs):
            raise RuntimeError("classifier infrastructure down")

        import main as _main

        with patch.object(_main, "run_agent_raw", side_effect=_boom):
            result = _run(g._check_toxicity(_StubCtx(), _StubAgent(), "Patient summary."))
        self.assertFalse(result.tripwire_triggered)
        self.assertIn("classifier_error", result.output_info)


# ─────────────────────────────────────────────────────────────────────────────
#  Integration: the FastAPI exception handler maps tripwires to 422 + structured detail
# ─────────────────────────────────────────────────────────────────────────────


class ExceptionHandlerTest(unittest.TestCase):
    """Exercise the FastAPI handler by raising the SDK exception directly.

    This avoids the cost of standing up the full pipeline; we only need to
    prove the @app.exception_handler is wired and shapes the response correctly.
    """

    def test_handler_returns_422_with_structured_detail(self) -> None:
        from fastapi.testclient import TestClient

        import main as _main

        # Create a tiny route that always raises the SDK tripwire so we can
        # observe the handler's response shape without invoking an LLM.
        # We piggyback onto the existing app by adding a probe route at runtime.
        from agents import OutputGuardrailTripwireTriggered
        from agents.guardrail import OutputGuardrailResult

        if "/_test/trip" not in {getattr(r, "path", "") for r in _main.app.router.routes}:
            @_main.app.get("/_test/trip")
            async def _probe():  # type: ignore[no-redef]
                # Build a minimal OutputGuardrailResult that mirrors a real trip.
                fake_output = GuardrailFunctionOutput(
                    output_info={
                        "failed_check": "schema",
                        "code": "consensus_schema_invalid",
                        "message": "test message",
                    },
                    tripwire_triggered=True,
                )
                fake_result = OutputGuardrailResult(
                    guardrail=g.consensus_output_guardrail,
                    agent_output="<raw>",
                    agent=_main.consensus_agent,
                    output=fake_output,
                )
                raise OutputGuardrailTripwireTriggered(fake_result)

        client = TestClient(_main.app)
        resp = client.get("/_test/trip")
        self.assertEqual(resp.status_code, 422)
        body = resp.json()
        self.assertEqual(body["detail"]["code"], "output_guardrail_failed")
        self.assertEqual(body["detail"]["subcode"], "consensus_schema_invalid")
        self.assertEqual(body["detail"]["guardrail"], "consensus_output_check")
        self.assertIn("Chen", body["detail"]["stage"])  # consensus agent name


