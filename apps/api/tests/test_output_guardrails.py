class _StubAgent:
    name = "stub-agent"


class _StubCtx:
    """Stand-in for RunContextWrapper. Only the .context attribute is read."""

    def __init__(self, context: Any = None) -> None:
        self.context = context


def _run(coro):
    return asyncio.get_event_loop().run_until_complete(coro)


def _valid_consensus_json() -> str:
    return json.dumps(
        {
            "primaryDiagnosis": "Migraine",
            "icdCode": "G43.909",
            "confidence": 82,
            "differentials": ["Tension headache", "Cluster headache"],
            "prognosis": "Good with proper management",
            "keyFindings": "Recurrent unilateral headache with photophobia",
            "urgency": "routine",
        }
    )


def _valid_plan_text() -> str:
    return """
## IMMEDIATE ACTIONS
Hydrate, rest in a dark room.

## MEDICATIONS TO CONSIDER
NSAID first-line; triptan if recurrent.

## DIAGNOSTIC TESTS
Consider MRI if red flags emerge.

## LIFESTYLE MODIFICATIONS
Sleep hygiene, regular meals, hydration.

## FOLLOW-UP SCHEDULE
2-week follow-up with neurology if symptoms persist.

## WARNING SIGNS
Sudden worst-ever headache, neurologic deficits.
"""


def _valid_message_text() -> str:
    return (
        "Thank you for sharing what you've been going through. After reviewing your "
        "symptoms, the most likely cause is migraine — a kind of headache that can "
        "feel overwhelming and is sometimes accompanied by sensitivity to light. "
        "Hydration, rest, and a quiet dark room often help. If your symptoms get "
        "worse or you notice any of the warning signs we've outlined, please act "
        "quickly. Remember, this guidance is generated by an AI advisory system and "
        "should not replace seeing your physician — please consult a licensed "
        "doctor before starting or changing treatment."
    )


