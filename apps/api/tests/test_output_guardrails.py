class ConsensusGuardrailTest(unittest.TestCase):
    def _check(self, raw: str) -> GuardrailFunctionOutput:
        return _run(g._check_consensus_output(_StubCtx(), _StubAgent(), raw))

    def test_valid_consensus_passes(self) -> None:
        result = self._check(_valid_consensus_json())
        self.assertFalse(result.tripwire_triggered)
        self.assertTrue(result.output_info.get("passed"))
        self.assertEqual(result.output_info["urgency"], "routine")

    def test_unparseable_trips(self) -> None:
        result = self._check("This is not JSON, just prose.")
        self.assertTrue(result.tripwire_triggered)
        self.assertEqual(result.output_info["code"], "consensus_unparseable")

    def test_bad_urgency_trips(self) -> None:
        bad = json.loads(_valid_consensus_json())
        bad["urgency"] = "asap"
        result = self._check(json.dumps(bad))
        self.assertTrue(result.tripwire_triggered)
        self.assertEqual(result.output_info["code"], "consensus_schema_invalid")

    def test_confidence_out_of_range_trips(self) -> None:
        bad = json.loads(_valid_consensus_json())
        bad["confidence"] = 150
        result = self._check(json.dumps(bad))
        self.assertTrue(result.tripwire_triggered)
        self.assertEqual(result.output_info["code"], "consensus_schema_invalid")

    def test_missing_required_field_trips(self) -> None:
        bad = json.loads(_valid_consensus_json())
        del bad["primaryDiagnosis"]
        result = self._check(json.dumps(bad))
        self.assertTrue(result.tripwire_triggered)
        self.assertEqual(result.output_info["code"], "consensus_schema_invalid")

    def test_bad_icd_format_trips(self) -> None:
        bad = json.loads(_valid_consensus_json())
        bad["icdCode"] = "MRI-Brain"
        result = self._check(json.dumps(bad))
        self.assertTrue(result.tripwire_triggered)
        self.assertEqual(result.output_info["code"], "consensus_icd_invalid")

    def test_empty_icd_lenient_default_passes(self) -> None:
        with patch.dict(os.environ, {"STRICT_ICD": ""}, clear=False):
            ok = json.loads(_valid_consensus_json())
            ok["icdCode"] = ""
            result = self._check(json.dumps(ok))
            self.assertFalse(result.tripwire_triggered)

    def test_empty_icd_strict_trips(self) -> None:
        with patch.dict(os.environ, {"STRICT_ICD": "1"}, clear=False):
            ok = json.loads(_valid_consensus_json())
            ok["icdCode"] = ""
            result = self._check(json.dumps(ok))
            self.assertTrue(result.tripwire_triggered)
            self.assertEqual(result.output_info["code"], "consensus_icd_invalid")

    def test_fenced_json_passes(self) -> None:
        fenced = "```json\n" + _valid_consensus_json() + "\n```"
        result = self._check(fenced)
        self.assertFalse(result.tripwire_triggered)


