class MessageGuardrailTest(unittest.TestCase):
    def _check(self, text: str, ctx_consensus: dict | None = None) -> GuardrailFunctionOutput:
        ctx = _StubCtx({"consensus": ctx_consensus} if ctx_consensus is not None else None)
        return _run(g._check_message(ctx, _StubAgent(), text))

    def test_disclaimer_at_end_passes(self) -> None:
        result = self._check(_valid_message_text())
        self.assertFalse(result.tripwire_triggered)

    def test_no_disclaimer_trips(self) -> None:
        text = (
            "Thanks for sharing your symptoms. The most likely cause is migraine. "
            "Hydrate, rest, and avoid bright lights. Take care."
        )
        result = self._check(text)
        self.assertTrue(result.tripwire_triggered)
        self.assertEqual(result.output_info["code"], "message_disclaimer_missing")

    def test_disclaimer_only_in_middle_trips(self) -> None:
        # AI/physician/consult mention is buried in the middle 70%; tail is bare.
        text = (
            "Thank you for reaching out. This is an AI advisory system — please "
            "consult your physician for any medical decisions. "
            + ("Now, about your symptoms: you described migraine-like episodes. " * 8)
            + "I hope you feel better soon."
        )
        result = self._check(text)
        self.assertTrue(result.tripwire_triggered)
        self.assertEqual(result.output_info["code"], "message_disclaimer_missing")

    def _disclaimer_tail(self) -> str:
        # Long enough to keep the AI/physician/consult tokens in the last 30%
        # of the full message even after a clinical paragraph is appended.
        return (
            " Remember, this guidance is generated by an AI advisory system and "
            "should not replace seeing your physician — please consult a "
            "licensed doctor before starting or changing treatment."
        )

    def test_hallucination_check_disabled_by_default(self) -> None:
        with patch.dict(os.environ, {"MESSAGE_HALLUCINATION_CHECK": ""}, clear=False):
            consensus = {
                "primaryDiagnosis": "Migraine",
                "differentials": ["Tension headache"],
            }
            body = "Thank you for sharing your symptoms. The team also considered fibrosis briefly."
            text = body + self._disclaimer_tail()
            result = self._check(text, ctx_consensus=consensus)
            # Default off — passes despite intruder term.
            self.assertFalse(result.tripwire_triggered)

    def test_hallucination_check_enabled_trips(self) -> None:
        with patch.dict(os.environ, {"MESSAGE_HALLUCINATION_CHECK": "1"}, clear=False):
            consensus = {
                "primaryDiagnosis": "Migraine",
                "differentials": ["Tension headache"],
            }
            # "fibrosis" matches the dx-token regex (-osis) and is not in consensus.
            body = "Thank you for sharing your symptoms. The team also considered fibrosis briefly."
            text = body + self._disclaimer_tail()
            result = self._check(text, ctx_consensus=consensus)
            self.assertTrue(result.tripwire_triggered)
            self.assertEqual(
                result.output_info["code"], "message_introduces_unknown_diagnosis"
            )

    def test_hallucination_check_enabled_consensus_terms_pass(self) -> None:
        with patch.dict(os.environ, {"MESSAGE_HALLUCINATION_CHECK": "1"}, clear=False):
            consensus = {
                "primaryDiagnosis": "Pulmonary fibrosis",
                "differentials": ["Sarcoidosis"],
            }
            body = (
                "Thank you for sharing your symptoms. The team weighed pulmonary "
                "fibrosis against sarcoidosis."
            )
            text = body + self._disclaimer_tail()
            result = self._check(text, ctx_consensus=consensus)
            self.assertFalse(result.tripwire_triggered)


